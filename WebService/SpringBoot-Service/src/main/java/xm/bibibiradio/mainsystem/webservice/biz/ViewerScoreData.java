package xm.bibibiradio.mainsystem.webservice.biz;

public class ViewerScoreData {
    private String viewerName;
    private String viewerUrl;
    private String viewerShowUrl;
    private long viewNum;
    private int site;
    private String category;
    private long level;
    private long score;
    
    public String getViewerName() {
        return viewerName;
    }
    public void setViewerName(String viewerName) {
        this.viewerName = viewerName;
    }
    public String getViewerUrl() {
        return viewerUrl;
    }
    public void setViewerUrl(String viewerUrl) {
        this.viewerUrl = viewerUrl;
    }
    public String getViewerShowUrl() {
        return viewerShowUrl;
    }
    public void setViewerShowUrl(String viewerShowUrl) {
        this.viewerShowUrl = viewerShowUrl;
    }
    public long getViewNum() {
        return viewNum;
    }
    public void setViewNum(long viewNum) {
        this.viewNum = viewNum;
    }
    public int getSite() {
        return site;
    }
    public void setSite(int site) {
        this.site = site;
    }
    public String getCategory() {
        return category;
    }
    public void setCategory(String category) {
        this.category = category;
    }
    public long getLevel() {
        return level;
    }
    public void setLevel(long level) {
        this.level = level;
    }
    public long getScore() {
        return score;
    }
    public void setScore(long score) {
        this.score = score;
    }
    
    
}
